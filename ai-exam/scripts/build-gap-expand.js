function j(id, stem, correct, explain) {
  return {
    id,
    module: "AI",
    type: "judge",
    stem,
    options: ["正确", "错误"],
    answer: [correct ? 0 : 1],
    explain,
  };
}
function sQ(id, stem, options, ai, explain) {
  return { id, module: "AI", type: "single", stem, options, answer: [ai], explain };
}
function mQ(id, stem, options, ans, explain) {
  return {
    id,
    module: "AI",
    type: "multi",
    stem,
    options,
    answer: ans.slice().sort((a, b) => a - b),
    explain,
  };
}

